console.log(true);
console.log(false);

console.log(3 > 7);
console.log(7 > 3);

console.log(typeof (3 > 7));

console.log(3 >= 3);
console.log(3 == 3);
console.log(3 === 3);

// 데이터타입 달라도 값 같으면 true
console.log(3 =='3');

// 데이터타입 같고 값도 같아야 true
console.log(3 === '3');

console.log(true && true);  // and 연산. 양쪽 다 true 일 때 true
console.log(true && false);

console.log(true || true);  // or. 한쪽만 true 면 true
console.log(true || false);
console.log(false || false);

console.log(!true);  // not
console.log(!false);

console.log((7 > 3) && (6 < -1));
console.log((7 > 3) || (6 < -1));
console.log(!(7 > 3));